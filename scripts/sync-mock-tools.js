#!/usr/bin/env node
/**
 * Sync Mock Tools — auto-generate MOCK_TOOLS from live MCP server schemas.
 *
 * Connects to each MCP server via stdio, calls tools/list, and writes
 * a TypeScript file with the complete tool definitions for live-harness.ts.
 *
 * Usage:
 *   node scripts/sync-mock-tools.js              # Generate from live servers
 *   node scripts/sync-mock-tools.js --check      # Compare generated vs current (exit 1 if stale)
 *   node scripts/sync-mock-tools.js --dry-run    # Print without writing
 *
 * Output:
 *   evals/generated/mock-tool-defs.ts
 *
 * Prerequisites:
 *   - `az login` for CRM server
 *   - OBSIDIAN_VAULT_PATH in .env for OIL server
 */

import { Client } from "@modelcontextprotocol/sdk/client/index.js";
import { StdioClientTransport } from "@modelcontextprotocol/sdk/client/stdio.js";
import { writeFileSync, readFileSync, mkdirSync, existsSync } from "node:fs";
import { join, resolve, dirname } from "node:path";
import { createHash } from "node:crypto";

const ROOT = resolve(dirname(new URL(import.meta.url).pathname), "..");
const OUTPUT_DIR = join(ROOT, "evals", "generated");
const OUTPUT_PATH = join(OUTPUT_DIR, "mock-tool-defs.ts");

// ── CLI ─────────────────────────────────────────────────────────────────────

const args = process.argv.slice(2);
const CHECK_MODE = args.includes("--check");
const DRY_RUN = args.includes("--dry-run");

// ── Server configs (mirrors mcp.json) ───────────────────────────────────────

function getServerConfigs() {
  const msxStart = join(ROOT, "scripts", "msx-start.js");
  const oilStart = join(ROOT, "scripts", "oil-start.js");

  const servers = [];

  if (existsSync(msxStart)) {
    servers.push({
      name: "msx-crm",
      prefix: "msx_crm__",
      mcpPrefix: "msx-crm:",
      command: "node",
      args: [msxStart],
    });
  }

  if (existsSync(oilStart)) {
    servers.push({
      name: "oil",
      prefix: "oil__",
      mcpPrefix: "oil:",
      command: "node",
      args: [oilStart],
    });
  }

  return servers;
}

// ── Connect and list tools ──────────────────────────────────────────────────

async function listServerTools(serverConfig) {
  const transport = new StdioClientTransport({
    command: serverConfig.command,
    args: serverConfig.args,
  });

  const client = new Client({ name: "sync-mock-tools", version: "1.0.0" });

  try {
    await client.connect(transport);
    const result = await client.listTools();
    return result.tools ?? [];
  } finally {
    try { await client.close(); } catch { /* ignore */ }
  }
}

// ── Convert MCP tool schema to OpenAI function-calling format ───────────────

function toOpenAITool(tool, serverConfig) {
  const fnName = `${serverConfig.prefix}${tool.name}`;
  const schema = tool.inputSchema ?? { type: "object", properties: {} };

  return {
    type: "function",
    function: {
      name: fnName,
      description: tool.description ?? "",
      parameters: {
        type: "object",
        properties: schema.properties ?? {},
        ...(schema.required?.length ? { required: schema.required } : {}),
      },
    },
  };
}

// ── Generate TypeScript source ──────────────────────────────────────────────

function generateTypeScript(allTools) {
  const toolsJson = JSON.stringify(allTools, null, 2);
  const hash = createHash("sha256").update(toolsJson).digest("hex").slice(0, 12);

  return `/**
 * AUTO-GENERATED by scripts/sync-mock-tools.js — DO NOT EDIT MANUALLY.
 *
 * Generated: ${new Date().toISOString()}
 * Schema hash: ${hash}
 * Tool count: ${allTools.length}
 *
 * To regenerate: node scripts/sync-mock-tools.js
 */

// @ts-nocheck — auto-generated, types are structurally correct
export const MOCK_TOOLS = ${toolsJson} as const;

/** Schema version hash — changes when tool definitions change. */
export const SCHEMA_VERSION = "${hash}";

/** Sorted tool names for schema comparison. */
export const TOOL_NAMES = ${JSON.stringify(allTools.map((t) => t.function.name).sort(), null, 2)};
`;
}

// ── Check mode: compare generated hash vs existing ──────────────────────────

function checkStaleness(newContent) {
  if (!existsSync(OUTPUT_PATH)) {
    console.error("❌ No generated file found. Run: node scripts/sync-mock-tools.js");
    process.exit(1);
  }

  const existing = readFileSync(OUTPUT_PATH, "utf-8");
  const existingHash = existing.match(/Schema hash: ([a-f0-9]+)/)?.[1];
  const newHash = newContent.match(/Schema hash: ([a-f0-9]+)/)?.[1];

  if (existingHash === newHash) {
    console.log("✅ Mock tool definitions are up to date.");
    process.exit(0);
  } else {
    console.error(`❌ Mock tool definitions are stale.`);
    console.error(`   Existing: ${existingHash}`);
    console.error(`   Current:  ${newHash}`);
    console.error(`   Run: node scripts/sync-mock-tools.js`);
    process.exit(1);
  }
}

// ── Fallback: extract tool names from existing MOCK_TOOLS in live-harness ───

function extractFromLiveHarness() {
  const harnessPath = join(ROOT, "evals", "live", "live-harness.ts");
  if (!existsSync(harnessPath)) return null;

  const content = readFileSync(harnessPath, "utf-8");
  const toolNames = [];
  const nameRe = /name:\s*"([^"]+)"/g;
  let match;
  while ((match = nameRe.exec(content)) !== null) {
    // Only capture tool function names (those with __ separator)
    if (match[1].includes("__")) {
      toolNames.push(match[1]);
    }
  }
  return toolNames;
}

// ── Main ────────────────────────────────────────────────────────────────────

async function main() {
  const servers = getServerConfigs();
  const allTools = [];
  let usedFallback = false;

  if (servers.length === 0) {
    console.log("⚠️  No MCP server start scripts found. Using fallback from live-harness.ts.");
    usedFallback = true;
  }

  for (const server of servers) {
    try {
      console.log(`📡 Connecting to ${server.name}...`);
      const tools = await listServerTools(server);
      console.log(`   Found ${tools.length} tools`);

      for (const tool of tools) {
        allTools.push(toOpenAITool(tool, server));
      }
    } catch (err) {
      console.warn(`⚠️  Failed to connect to ${server.name}: ${err.message}`);
      console.warn(`   Falling back to existing definitions in live-harness.ts`);
      usedFallback = true;
    }
  }

  // If no live servers available, extract current tool names from live-harness.ts
  // as a validation baseline (the existing MOCK_TOOLS remain the source of truth)
  if (allTools.length === 0) {
    const existing = extractFromLiveHarness();
    if (existing && existing.length > 0) {
      console.log(`📄 Extracted ${existing.length} tool names from live-harness.ts (fallback mode)`);
      // In fallback mode, we only validate names — can't regenerate full schemas
      const hash = createHash("sha256").update(existing.sort().join(",")).digest("hex").slice(0, 12);
      const content = `/**
 * AUTO-GENERATED by scripts/sync-mock-tools.js — FALLBACK MODE (no live servers).
 *
 * Generated: ${new Date().toISOString()}
 * Schema hash: ${hash}
 * Tool count: ${existing.length}
 * Mode: fallback — tool names only, no parameter schemas
 *
 * To regenerate with full schemas: start MCP servers then run scripts/sync-mock-tools.js
 */

/** Schema version hash — changes when tool definitions change. */
export const SCHEMA_VERSION = "${hash}";

/** Sorted tool names extracted from live-harness.ts MOCK_TOOLS. */
export const TOOL_NAMES = ${JSON.stringify(existing.sort(), null, 2)};
`;

      if (CHECK_MODE) {
        checkStaleness(content);
        return;
      }

      if (DRY_RUN) {
        console.log("\n--- Generated (fallback) ---\n");
        console.log(content);
        return;
      }

      mkdirSync(OUTPUT_DIR, { recursive: true });
      writeFileSync(OUTPUT_PATH, content);
      console.log(`✅ Wrote fallback tool definitions to ${OUTPUT_PATH}`);
      return;
    }

    console.error("❌ No tools found from servers or fallback.");
    process.exit(1);
  }

  // Generate full TypeScript with schemas
  const content = generateTypeScript(allTools);

  if (CHECK_MODE) {
    checkStaleness(content);
    return;
  }

  if (DRY_RUN) {
    console.log("\n--- Generated ---\n");
    console.log(content);
    console.log(`\n📊 ${allTools.length} tools across ${servers.length} server(s)`);
    if (usedFallback) console.log("⚠️  Some servers used fallback mode");
    return;
  }

  mkdirSync(OUTPUT_DIR, { recursive: true });
  writeFileSync(OUTPUT_PATH, content);
  console.log(`\n✅ Wrote ${allTools.length} tool definitions to ${OUTPUT_PATH}`);
  if (usedFallback) console.log("⚠️  Some servers used fallback — re-run with live servers for full schemas");
}

main().catch((err) => {
  console.error("Fatal:", err.message);
  process.exit(1);
});
